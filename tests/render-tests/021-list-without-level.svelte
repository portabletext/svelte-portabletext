<h2>In-person access: Research appointments</h2>
<p>
  The collection may be examined by arranging a research appointment <strong>in advance</strong> by contacting
  the ACT archivist by email or phone. ACT generally does not accept walk-in research patrons, although
  requests may be made in person at the Archivist’s office (E15-222). ACT recommends arranging appointments
  at least three weeks in advance in order to ensure availability. ACT reserves the right to cancel research
  appointments at any time. Appointment scheduling is subject to institute holidays and closings.
</p>
<p>
  The collection space is located at:<br />20 Ames Street<br />Building E15-235<br />Cambridge,
  Massachusetts 02139
</p>
<h2>In-person access: Space policies</h2>
<ul>
  <li>The Archivist or an authorized ACT staff member must attend researchers at all times.</li>
  <li>
    No pens, markers, or adhesives (e.g. “Post-it” notes) are permitted in the collection space;
    pencils will be provided upon request.
  </li>
  <li>
    Cotton gloves must be worn when handling collection materials; gloves will be provided by the
    Archivist.
  </li>
  <li>No food or beverages are permitted in the collection space.</li>
  <li>
    Laptop use is permitted in the collection space, as well as digital cameras and cellphones.
    Unless otherwise authorized, any equipment in the collection space (including but not limited to
    computers, telephones, scanners, and viewing equipment) is for use by ACT staff members only.
  </li>
  <li>
    Photocopying machines in the ACT hallway will be accessible by patrons under the supervision of
    the Archivist.
  </li>
  <li>Patrons may only browse materials that have been made available for access.</li>
</ul>
<h2>Remote access: Reference requests</h2>
<p>
  For patrons who are unable to arrange for an on-campus visit to the Archives and Special
  Collections, reference questions may be directed to the Archivist remotely by email or phone.
  Generally, emails and phone calls will receive a response within 72 hours of receipt. Requests are
  typically filled in the order they are received.
</p>
<h2><strong>Use of patron information</strong></h2>
<p>
  Patrons requesting collection materials in person or remotely may be asked to provide certain
  information to the Archivist, such as contact information and topic(s) of research. This
  information is only used to track requests for statistical evaluations of collection use and will
  not be disclosed to outside organizations for any purpose. ACT will endeavor to protect the
  privacy of all patrons accessing collections.
</p>
<h2><strong>Fees</strong></h2>
<p>
  ACT reserves the right to charge an hourly rate for requests that require more than three hours of
  research on behalf of a patron (remote requests). Collection materials may be scanned and made
  available upon request, but digitization of certain materials may incur costs. Additionally,
  requests to publish, exhibit, or otherwise reproduce and display collection materials may incur
  use fees.
</p>
<h2><strong>Use of MIT-owned materials by patrons</strong></h2>
<p>
  Permission to examine collection materials in person or remotely (by receiving transfers of
  digitized materials) does not imply or grant permission to publish or exhibit those materials.
  Permission to publish, exhibit, or otherwise use collection materials is granted on a case by case
  basis in accordance with MIT policy, restrictions that may have been placed on materials by donors
  or depositors, and copyright law. To request permission to publish, exhibit, or otherwise use
  collection materials, contact the Archivist. <strong
    >When permission is granted by MIT, patrons must comply with all guidelines provided by ACT for
    citations, credits, and copyright statements. Exclusive rights to examine or publish material
    will not be granted.</strong
  >
</p>
